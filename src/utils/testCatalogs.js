export function buildReportOrderIndex(testType) {
  const categories = REPORT_CATEGORIES_BY_TYPE[testType] || {};
  const index = new Map();
  let order = 0;

  Object.entries(categories).forEach(([category, items]) => {
    items.forEach((item) => {
      index.set(`${category}::${item}`, order);
      order += 1;
    });
  });

  return index;
}
